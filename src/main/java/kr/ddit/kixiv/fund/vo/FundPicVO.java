package kr.ddit.kixiv.fund.vo;

public class FundPicVO {
	private String picseq; 
	private String fund_id;
	private String pic_num;
	private String pic_path;
	
	FundPicVO() {}

	public String getPicseq() {
		return picseq;
	}

	public void setPicseq(String picseq) {
		this.picseq = picseq;
	}

	public String getFund_id() {
		return fund_id;
	}

	public void setFund_id(String fund_id) {
		this.fund_id = fund_id;
	}

	public String getPic_num() {
		return pic_num;
	}

	public void setPic_num(String pic_num) {
		this.pic_num = pic_num;
	}

	public String getPic_path() {
		return pic_path;
	}

	public void setPic_path(String pic_path) {
		this.pic_path = pic_path;
	}

	@Override
	public String toString() {
		return "FundPicVO [picseq=" + picseq + ", fund_id=" + fund_id + ", pic_num=" + pic_num + ", pic_path="
				+ pic_path + "]";
	}
}
