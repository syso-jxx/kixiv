package kr.ddit.kixiv.blackUser.vo;

import java.util.Date;

public class BlackUserVO {
	private int blackNum;
	private String blackReason;
	private Date blackDate;
	private String userId;
	
	public int getBlackNum() {
		return blackNum;
	}
	public void setBlackNum(int blackNum) {
		this.blackNum = blackNum;
	}
	public String getBlackReason() {
		return blackReason;
	}
	public void setBlackReason(String blackReason) {
		this.blackReason = blackReason;
	}
	public Date getBlackDate() {
		return blackDate;
	}
	public void setBlackDate(Date blackDate) {
		this.blackDate = blackDate;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	
	@Override
	public String toString() {
		return "BlackUserVO [blackNum=" + blackNum + ", blackReason=" + blackReason + ", blackDate=" + blackDate
				+ ", userId=" + userId + "]";
	}
	
}
