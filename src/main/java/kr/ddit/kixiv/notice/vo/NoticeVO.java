package kr.ddit.kixiv.notice.vo;

import java.util.Date;

public class NoticeVO {
	private int notice_id;
	private String notice_title;
	private String notice_content;
	private int hit;
	private Date notice_sdate;
	private String admin_id;

	public int getNotice_id() {
		return notice_id;
	}
	public void setNotice_id(int notice_id) {
		this.notice_id = notice_id;
	}
	public String getNotice_title() {
		return notice_title;
	}
	public void setNotice_title(String notice_title) {
		this.notice_title = notice_title;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public int getHit() {
		return hit;
	}
	public void setHit(int hit) {
		this.hit = hit;
	}
	public Date getNotice_sdate() {
		return notice_sdate;
	}
	public void setNotice_sdate(Date notice_sdate) {
		this.notice_sdate = notice_sdate;
	}
	public String getAdmin_id() {
		return admin_id;
	}
	public void setAdmin_id(String admin_id) {
		this.admin_id = admin_id;
	}

	@Override
	public String toString() {
		return "NoticeVO [notice_id=" + notice_id + ", notice_title=" + notice_title + ", notice_content="
				+ notice_content + ", hit=" + hit + ", notice_sdate=" + notice_sdate + ", admin_id=" + admin_id + "]";
	}
	
}
